    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // 1. Captura de datos desde los campos
    String nombre = txtNombre.getText();
    String correo = txtCorreo.getText();
    String contrasena = txtContrasena.getText();
    String rol = txtRol.getText(); // o txtRol.getText()

    Connection conn = null;
    CallableStatement stmt = null;

    try {
        // 2. Conexión y llamado al procedimiento
        conn = Conexion.getConexion(); // Usa tu clase de conexión
        stmt = conn.prepareCall("{CALL sp_registrar_usuario(?, ?, ?, ?)}");

        stmt.setString(1, nombre);
        stmt.setString(2, correo);
        stmt.setString(3, contrasena);
        stmt.setString(4, rol);

        stmt.execute();

        // 3. Mensaje de éxito
        JOptionPane.showMessageDialog(null, "Usuario agregado exitosamente.");

        // 4. Limpiar campos si gustas
        txtNombre.setText("");
        txtCorreo.setText("");
        txtContrasena.setText("");
        txtRol.setText(""); // si usas JComboBox
        
        cargarBarberosEnTabla();

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al agregar usuario: " + ex.getMessage());
    } finally {
        try {
            if (stmt != null) stmt.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

