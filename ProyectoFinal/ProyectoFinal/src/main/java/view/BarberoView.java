    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        try {
    Connection con = Conexion.getConexion();
    CallableStatement stmt = con.prepareCall("{CALL sp_modificar_usuario(?, ?, ?, ?, ?)}");

    // Suponiendo que tienes campos como estos:
    int id = Integer.parseInt(txtID.getText());
    String nombre = txtNombre.getText();
    String correo = txtCorreo.getText();
    String contrasena = txtContrasena.getText();
    String rol = txtRol.getText();

    stmt.setInt(1, id);
    stmt.setString(2, nombre);
    stmt.setString(3, correo);
    stmt.setString(4, contrasena);
    stmt.setString(5, rol);

    stmt.execute();

    JOptionPane.showMessageDialog(null, "Usuario modificado exitosamente.");

    // Limpiar campos
    txtID.setText("");
    txtNombre.setText("");
    txtCorreo.setText("");
    txtContrasena.setText("");
    txtRol.setText("");

    // Recargar tabla
    cargarBarberosEnTabla();

} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Error al modificar el usuario: " + e.getMessage());
}

    }//GEN-LAST:event_BtnModificarActionPerformed
    
